# Declarando variaveis
print("Eu tenho",3,"cachorros")

# Declarando minha primeira variável
num = 12
print("Eu tenho",num,"cachorros")

# Declarando duas ou mais variáveis
x = 10
y = 12
print(x)
print(y)

# Declarando que a segunda variável tem o mesmo valor da primeira
x = 10
x = y
print(x)
print(y)

# Declarando várias variáveis na mesma linha
x,y = 1,2
print(x,y, sep=",")

# Declarando variáveis com acentuação. Veio a partir do python 3
ação = 10
print(ação)

# Tipos de variáveis
"""
int -> Inteira
float -> Ponto flutuante
round -> Arredondamento
"""
num1 = 25
print(int(num1))
print(float(num1))
num2 = 2.7
print(round(num2))